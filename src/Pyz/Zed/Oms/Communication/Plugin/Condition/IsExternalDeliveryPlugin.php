namespace Pyz\Zed\Oms\Communication\Plugin\Condition;

use Orm\Zed\Sales\Persistence\SpySalesOrderItem;
use Spryker\Zed\Kernel\Communication\AbstractPlugin;
use Spryker\Zed\Oms\Dependency\Plugin\Condition\ConditionInterface;


class IsExternalDeliveryPlugin extends AbstractPlugin implements ConditionInterface
{
    /**
     * {@inheritDoc}
     *
     * @api
     *
     * @param \Orm\Zed\Sales\Persistence\SpySalesOrderItem $orderItem
     *
     * @return bool
     */
    public function check(SpySalesOrderItem $orderItem)
    {
        file_put_contents(APPLICATION_ROOT_DIR.'/tmp.log',$orderItem->getPriceToPayAggregation().PHP_EOL, FILE_APPEND);

        if (($orderItem->getPriceToPayAggregation() /100) < 100) {
            return true;
        }

        return false;
    }
}
